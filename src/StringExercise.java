public class StringExercise {
    public static void main(String[] args) {

        String str = "";

        System.out.println(str.replace("", "We don't need no education"));
        System.out.println(str.replace("", "We don't need no thought control"));
        System.out.println(str.replace("", "Check \"this\" out!, \"s inside of \"s!"));
        System.out.println(str.replace("", "In windows, the main drive is usually C:\\"));
        System.out.println(str.replace("", "I can do backslashes \\, double backslashes \\\\,"));
        System.out.println(str.replace("", "and the amazing triple backslash \\\\\\!"));
    }
}
